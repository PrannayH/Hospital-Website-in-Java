package com.example.demo.model;


public class Habits {
	private Boolean alcohol;
	private String alcoholText;
	private Boolean smoking;
	private String smokingText;
	private Boolean sleep;
	private String sleepText;
	private Boolean bowel;
	private String bowelText;
	public Boolean getAlcohol() {
		return alcohol;
	}
	public void setAlcohol(Boolean alcohol) {
		this.alcohol = alcohol;
	}
	public String getAlcoholText() {
		return alcoholText;
	}
	public void setAlcoholText(String alcoholText) {
		this.alcoholText = alcoholText;
	}
	public Boolean getSmoking() {
		return smoking;
	}
	public void setSmoking(Boolean smoking) {
		this.smoking = smoking;
	}
	public String getSmokingText() {
		return smokingText;
	}
	public void setSmokingText(String smokingText) {
		this.smokingText = smokingText;
	}
	public Boolean getSleep() {
		return sleep;
	}
	public void setSleep(Boolean sleep) {
		this.sleep = sleep;
	}
	public String getSleepText() {
		return sleepText;
	}
	public void setSleepText(String sleepText) {
		this.sleepText = sleepText;
	}
	public Boolean getBowel() {
		return bowel;
	}
	public void setBowel(Boolean bowel) {
		this.bowel = bowel;
	}
	public String getBowelText() {
		return bowelText;
	}
	public void setBowelText(String bowelText) {
		this.bowelText = bowelText;
	}
	
	
	
}
